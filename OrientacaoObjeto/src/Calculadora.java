
public class Calculadora {

	public static void main(String[] args) {
 double n1 = 10;
 double n2  = 2;
 double resulso;
 double resulsu;
 double resuldiv;
 double resulmut;
	
 resulso = n1 + n2; //Soma 2 numeros e guarda na variavel resulso
 	System.out.println("Resultado da soma é : "+resulso);
		
 resulsu = n1 - n2;//subtrai 2 numeros e guarda na variavel resulsu
 	System.out.println("Resultado da subtração é : "+resulsu);
		
 resuldiv = n1 / n2;//Divide 2 numeros e guarda na variavel resuldiv
 	System.out.println("Resultado da divisão é : "+resuldiv);
		
 resulmut = n1 * n2;//Mutiplica 2 numeros e guarda na variavel resulmut
 	System.out.println("Resultado da mutiplicação é : "+resulmut);
	}
}	
