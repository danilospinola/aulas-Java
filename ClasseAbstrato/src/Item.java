
public abstract class Item {
	
	private int posx;
	private int posy;
	
	public abstract void pegar();
	
	//getter e setter
	public int getPosx() {
		return posx;
	}
	public void setPosx(int posx) {
		this.posx = posx;
	}
	public int getPosy() {
		return posy;
	}
	public void setPosy(int posy) {
		this.posy = posy;
	}
}
