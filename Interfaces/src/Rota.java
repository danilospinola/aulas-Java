
public class Rota {

	public void ir(Automovel automovel) { // Requer um automovel para realizar o método.
		automovel.virarDireita(); // Realiza o método virar a direita do automovel
		automovel.acelerar(); // Realiza o método acelerar do automovel
		automovel.virarEsquerda(); // Realiza o método virar a esquerda do automovel
		automovel.virarDireita(); // Realiza o método virar a direita do automovel
	}
}
