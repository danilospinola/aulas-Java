
public class Areas {

	int calculararea(int x, int y) { // Pega 2 numeros e calcula a área de um triangulo

		return (x * y) / 2;
	}

	double calculararea(double x, double y) { // Pega 2 numeros divide por 2 e calcula a área de um retangulo

		return (x * y);
	}

	int calculararea(int x) { //pega um numero e calcula a área de um quadrado

		return (x * x);
	}
}
